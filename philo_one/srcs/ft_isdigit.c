int		ft_isdigit(int c)
{
	return ((c >= '0' && c <= '9') ? 1 : 0);
}

int		ft_is_all_digit_in_str(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(ft_isdigit(str[i])))
			return (0);
		i++;
	}
	return (1);
}
